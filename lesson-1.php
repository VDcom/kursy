            <h2 class="section-title">Таргетинг</h2>
            <p>Та́ргетинг (англ. target — цель или «мишень») — рекламный механизм, позволяющий выделить из всей имеющейся
                аудитории только ту часть, которая удовлетворяет заданным критериям (целевую аудиторию); пол, возраст,
                социальный статус, уровень дохода и прочее,  и показать рекламу именно ей.</p>

            <p>Важно проводить таргетинг для новых товаров или услуг, чтобы не «промахнуться»: каждый товар или услуга
                должен быть нацелен на группу потребителей, которые с наибольшей вероятностью заинтересуются данным
                товаром. Классический пример таргетинга: реклама пива в перерыве футбольного матча.</p>

            <p>Таргетинг это комплекс мероприятий по настройке рекламной кампании на определенную целевую аудиторию.</p>


